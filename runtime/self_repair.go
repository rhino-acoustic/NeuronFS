package main

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"time"
)

// ============================================================================
// Module: Self-Repair Suggester (Phase 45)
// Combines Similarity Index + Audit Trail to generate repair proposals
// when runtime errors occur. Proposals are dropped into _inbox/repair_proposals/
// for agent review — never auto-applied.
// ============================================================================

// SuggestRepair is called when an error is engraved. It searches for similar
// past errors and their successful resolutions, then generates a repair proposal.
func SuggestRepair(brainRoot, module, errMessage string) {
	if brainRoot == "" || globalSimilarityIndex == nil {
		return
	}

	// Step 1: Find similar neurons to this error
	query := fmt.Sprintf("%s %s", module, errMessage)
	similar := QuerySimilar(query, 5)
	if len(similar) == 0 {
		return
	}

	// Step 2: Scan audit trail for past successful repairs related to similar paths
	auditFile := filepath.Join(brainRoot, "hippocampus", "audit_trail", "audit.jsonl")
	auditData, err := os.ReadFile(auditFile)
	pastRepairs := []string{}
	if err == nil {
		lines := strings.Split(string(auditData), "\n")
		for _, line := range lines {
			line = strings.TrimSpace(line)
			if line == "" {
				continue
			}
			// Look for successful engrave actions related to similar paths
			if strings.Contains(line, "\"success\":true") {
				for _, sim := range similar {
					if strings.Contains(line, sim.Path) || strings.Contains(line, "engrave") {
						pastRepairs = append(pastRepairs, line)
						break
					}
				}
			}
		}
	}

	// Step 3: Build proposal content
	var sb strings.Builder
	sb.WriteString("---\n")
	sb.WriteString(fmt.Sprintf("module: %s\n", module))
	sb.WriteString(fmt.Sprintf("timestamp: %s\n", time.Now().Format(time.RFC3339)))
	sb.WriteString("type: repair_proposal\n")
	sb.WriteString("status: pending\n")
	sb.WriteString("---\n\n")
	sb.WriteString("# Self-Repair Proposal\n\n")
	sb.WriteString(fmt.Sprintf("## Error\n%s\n\n", errMessage))

	sb.WriteString("## Similar Past Neurons\n")
	for _, sim := range similar {
		sb.WriteString(fmt.Sprintf("- `%s` (score: %.3f)\n", sim.Path, sim.Score))
	}

	if len(pastRepairs) > 0 {
		sb.WriteString("\n## Past Successful Repairs\n")
		maxShow := 5
		if len(pastRepairs) < maxShow {
			maxShow = len(pastRepairs)
		}
		for _, repair := range pastRepairs[:maxShow] {
			sb.WriteString(fmt.Sprintf("```json\n%s\n```\n", repair))
		}
	}

	sb.WriteString("\n## Suggested Action\n")
	sb.WriteString("Review the similar neurons above and apply the repair pattern that resolved similar issues in the past.\n")
	sb.WriteString("\n> This proposal was auto-generated by Phase 45 Self-Repair Suggester.\n")
	sb.WriteString("> It will NOT be auto-applied. Human or agent review required.\n")

	// Step 4: Write proposal to _inbox/repair_proposals/
	proposalDir := filepath.Join(brainRoot, "_inbox", "repair_proposals")
	_ = os.MkdirAll(proposalDir, 0755)

	safeName := strings.ReplaceAll(module, "/", "_")
	safeName = strings.ReplaceAll(safeName, "\\", "_")
	if len(safeName) > 30 {
		safeName = safeName[:30]
	}
	fileName := fmt.Sprintf("repair_%s_%d.neuron", safeName, time.Now().Unix())
	filePath := filepath.Join(proposalDir, fileName)

	if writeErr := os.WriteFile(filePath, []byte(sb.String()), 0644); writeErr == nil {
		fmt.Printf("[Self-Repair] Proposal generated: %s\n", fileName)
		RecordAudit(brainRoot, "self_repair", "propose", filePath, errMessage, true)
	}
}
